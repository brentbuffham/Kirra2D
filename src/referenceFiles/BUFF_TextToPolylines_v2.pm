				($textX, $textY, $textZ) = $object->coordinates->i(0);
				#Get other text attributes
				$textAngle 	= $object->text->angle;
				#print "textAngle = $textAngle\n";
				$textScale 	= $object->text->scale;
				$textSize 	= $object->text->size;
				$textHeight = $object->text->height;
				$textFont 	= $object->text->font;
				 
				foreach my$i (0..$object->text->n -1)
				{
					# Get the text Value on each line
					$textValue[$i] = $object->text->i($i);
					$didTextChange = 1;
					
				}
				# Take the current layer
				my $layer = $layer1;
				#Ceate a new Layer --> removed 
				#my $layer = new Lava::Layer($layer1, "Supported characters converted");
				# Create and new Object
				my $polys = new Lava::Obj();
				# Set the polylines colour to the Text's colour
				$polys->colour($object->colour);
				# Use to count through points that get created from the character list
				my $point = 0;
				# For each line of text in the vulcan text object get the text value
				foreach my $row (0..$#textValue)
				{
					# For each character in the line of text provided get the character value
					foreach my $iPos (0..length($textValue[$row])-1)
					{
						# Assign the $char to the character provided by the previous loop.
						my $char = substr($textValue[$row],$iPos,1);
						
						# Check if the character polylines has a specified character available to convert
						if(exists($textData{$char}))
						{
							# Look up the polylines for $char 
							foreach my $i (0..$#{$textData{$char}})
							{
								# get the next point in the polyline list for that character
								my ($x,$y,$con) = @{$textData{$char}[$i]};
								
								#Apply tranformation to the points in the character polyline
								#                      ($xorig, $yorig, $rotA,      $x,          				$y)
								my ($xt,$yt) = transform($textX, $textY, $textAngle, 
												($x +($iPos* $posAdj))*($textHeight * $txtAdj), 
												($y - $row * $rowAdj)*($textHeight * $txtAdj ) 
												);
								
								# Assign the transformed point to the world coords in vulcan.
								$polys->coordinates->i($point, $xt, $yt, $textZ, 0, $con, $char); 
								# Increment to the point sequence by 1
								$point++;
							}
						}
					}	
				}
				# Add the $poly object to the vulcan Layer created 
				$layer->add($polys);
				# Notify Vulcan that the layer has been altered
				$layer->edited(1);
				@textValue = '';
			}
		}	
		
