###############################################
# Transform Sub Routine
##############################################
sub transform
{
	#First line provides the args to be passed to an empty list
	my ($xorig, $yorig, $rotA, $x, $y) = @_;
	#Vulcan returns the text angle in radians - no need to convert
	my $radians = $rotA;
	#Rotate the X value
	my $rotX = cos($radians)*$x - sin($radians)*$y;
	#Rotate the Y value
	my $rotY = sin($radians)*$x + cos($radians)*$y;
	# translate X to the world coordinates
	my $transX = $xorig+$rotX;
	# translate Y to the world coordinates
	my $transY = $yorig+$rotY;
	#return the rotated and translated coordinates
	return $transX, $transY;
}


