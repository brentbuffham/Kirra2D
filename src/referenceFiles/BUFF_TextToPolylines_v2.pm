{
	my($rotA) = @_;
	my $radians = $rotA*atan2(1,1)/45;
}

################################################
#Hides text with the object name starting with "HIDE_"
##################################################

sub hideText 
{
	
	for (my $j = 0; $j < @changedArray; $j++)
	{
 		RunMenu("VIEW_VIS_SETVIS","abort",
    	  sub { PanelResults("Action",
                "button_pressed" => "INVISIBLE"
                   ); },
      		sub { SelectObjects("*","$changedArray[$j]","*","*"); },
      		"SELECT_CANCEL",
      		sub { PanelResults("Action",
      	          "button_pressed" => "cancel"
                   ); },
     	 "FINISH") || print "Macro mismatch.\n";
  }
}

################################################
#Deletes text with the object name starting with "DELELE_TEXT"
##################################################
sub deleteText
{
	for (my $j = 0; $j < @changedArray; $j++)
	{
		RunMenu("DESIGN_OBJ_DELETE","abort",
      sub { SelectObjects("*","$changedArray[$j]","*","*"); },
      "SELECT_CANCEL",
      "FINISH") || print "Macro mismatch.\n";
	}
}

